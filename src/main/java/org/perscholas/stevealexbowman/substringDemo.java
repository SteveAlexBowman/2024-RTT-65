package org.perscholas.stevealexbowman;

public class substringDemo {
    public static void main(String[] args) {
        String str1 = "java is fun";


        System.out.println(str1.substring(0,4));   // extract substring from index 0 to 3
        System.out.println(str1.substring(5,7));   // extract substring from index 5 to 7
        System.out.println(str1.substring(8,11));   // extract substring from index 8 to 11


    }
}
