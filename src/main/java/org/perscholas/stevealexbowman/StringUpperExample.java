package org.perscholas.stevealexbowman;

public class StringUpperExample {
    public static void main(String[] args) {
        String s1 = "hello how are you";  // Notice how the string is all lower case
        String s1Upper = s1.toUpperCase();  // .toUpperCase() coverts the string to CAPITAL LETTERS when displayed
        System.out.println(s1Upper);  // Displaying the string to the console
    }
}
