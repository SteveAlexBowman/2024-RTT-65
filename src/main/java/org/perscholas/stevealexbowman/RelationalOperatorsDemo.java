package org.perscholas.stevealexbowman;

public class RelationalOperatorsDemo {
    public static void main(String[] args) {
        int x = 10, y =5;
        System.out.println("x > y : "+(x > y));
        System.out.println("x < y : "+(x < y));
        System.out.println("x >= y : "+(x >= y));
        System.out.println("x <= y : "+(x <= y));
        System.out.println("x == y : "+(x == y));
        System.out.println("x != y : "+(x != y));

        int variable1 = 50, variable2 = 100, variable3 = 50;
        System.out.println("variable 1 = " + variable1);
        System.out.println("variable 2 = " + variable2);
        System.out.println("variable 3 = " + variable3);
        System.out.println("variable1 == variable2: " + (variable1 == variable2));
        System.out.println("variable2 == variable3: " + (variable2 == variable3));
    }
}
