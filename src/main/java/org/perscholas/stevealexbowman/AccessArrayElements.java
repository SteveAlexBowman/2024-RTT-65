package org.perscholas.stevealexbowman;
/*
    Example of iterating over an Array using loops
 */
public class AccessArrayElements {
    public static void main(String[] args) {

        // Create an array
        int[]   age = {12, 4, 5, 2,5};

        // Access each array elements
        System.out.println("Accesing Elements of an Array:");
        System.out.println("First Element: " + age[0]);
        System.out.println("Second Element: " + age[1]);
        System.out.println("Third Element: " + age[2]);
        System.out.println("Fourth Element: " + age[4]);

    }
}
