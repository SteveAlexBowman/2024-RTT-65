package org.perscholas.stevealexbowman;

public class StringLowerExample {
    public static void main(String[] args) {
        String s1 = "HELLO HOW Are You?";  // notice the capital HELLO HOW
        String s1lower = s1.toLowerCase();  // the .toLowerCase() method ensures all characters are lowercase when displayed
        System.out.println(s1lower);
    }
}
